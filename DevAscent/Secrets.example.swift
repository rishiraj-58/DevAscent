/*
 HOW TO USE:
 
 1. Copy this file:
    cp Secrets.example.swift Secrets.swift
 
 2. Open Secrets.swift and replace "YOUR_GEMINI_API_KEY_HERE" with your actual key
 
 3. Get your Gemini API key from: https://aistudio.google.com/app/apikey
 
 4. Ensure Secrets.swift is in your .gitignore (it should be by default)
 
 FOR CI/CD (GitHub Actions):
 
 1. Go to your repo Settings > Secrets and variables > Actions
 
 2. Add secret: GEMINI_API_KEY = your_api_key
 
 3. In your workflow, set the environment variable:
    env:
      GEMINI_API_KEY: ${{ secrets.GEMINI_API_KEY }}
*/
